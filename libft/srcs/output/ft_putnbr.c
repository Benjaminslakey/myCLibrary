#include "../../includes/libft.h"

void	ft_putnbr(int n)
{
	long num;

	num = (long)n;
	if (num < 0)
	{
		num *= -1;
		ft_putchar('-');
	}
	if (num / 10)
		ft_putnbr(num / 10);
	ft_putchar((num % 10) + '0');
}
