#include "../../includes/libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *ret;

	ret = s1;
	while (*s1)
		s1++;
	while (n && (*s1++ = (char)*s2++))
		n--;
	*s1 = 0;
	return (ret);
}
