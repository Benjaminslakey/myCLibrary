#include "../../includes/libft.h"

void		ft_to_upper(char *str)
{
	char	c;
	int		i;

	i = 0;
	while ((c = str[i]) != 0)
	{
		*(str + i) += (LCASE(c)) ? (-32) : (0);
		i++;
	}
}
