#include "../../includes/libft.h"

void		ft_str_tolower(char *str)
{
	char	c;
	int		i;

	i = 0;
	while ((c = str[i]) != 0)
	{
		*(str + i) += (UCASE(c)) ? (32) : (0);
		i++;
	}
}
