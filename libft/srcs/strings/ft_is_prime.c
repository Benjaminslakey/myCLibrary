#include "../../includes/libft.h"

int		ft_is_prime(int num)
{
	int	i;

	i = 2;
	if (num < 2)
		return (0);
	while (i < num && ((i * i) <= num))
	{
		if ((num % i) == 0)
			return (0);
		if ((i % 2) == 0)
			i++;
		else
			i += 2;
	}
	return (1);
}
